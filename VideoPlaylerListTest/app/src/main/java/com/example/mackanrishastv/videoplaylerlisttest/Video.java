package com.example.mackanrishastv.videoplaylerlisttest;

public class Video {

    private String name;
    private int link;

    public Video(String name, int link) {
        this.name = name;
        this.link = link;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getLink() {
        return this.link;
    }

    public void setLink(int link) {
        this.link = link;
    }




}
