package com.example.mackanrishastv.question18;

public class SocialNetwork {

    private String nameSocial;
    private int imgSocial;

    public SocialNetwork(String nameSocial, int imgSocial) {
        this.nameSocial = nameSocial;
        this.imgSocial = imgSocial;
    }

    public String getNameSocial() {
        return nameSocial;
    }

    public void setNameSocial(String nameSocial) {
        this.nameSocial = nameSocial;
    }

    public int getImgSocial() {
        return imgSocial;
    }

    public void setImgSocial(int imgSocial) {
        this.imgSocial = imgSocial;
    }
}
